public class Sonda {
    int id;
    int posicaoX;
    int posicaoY;
    int direcao;

    public Sonda(int idNovo, int posicaoXInicial, int posicaoYInicial, int direcaoInicial){
        id = idNovo;
        posicaoX = posicaoXInicial;
        posicaoY = posicaoYInicial;
        direcao = direcaoInicial;
    }

}
